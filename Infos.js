var lastInfo=null;
function addInfo() {
	var inf = getInfo();
	while( inf == lastInfo ) inf = getInfo();
	lastInfo = inf;
	$("#infos .content").prepend( $('<p>'+inf.texte+' <a target="blank_" href="'+inf.source+'">[source]</a></p>') );
}